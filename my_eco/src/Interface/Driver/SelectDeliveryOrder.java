    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       if(((Driver)account.getEmployee()).getDeliveryOrder_ing() == null){
            int selectedrow = OrderjTable.getSelectedRow();
            DeliveryOrder d=(DeliveryOrder)OrderjTable.getValueAt(selectedrow, 0);
            ((Driver)account.getEmployee()).setDeliveryOrder_ing(d);
            ((Driver)account.getEmployee()).getDeliveryOrderDictionary().removeDeliveryOrder(d);
            
            for(Material m : d.getBuyorder().getMaterials().keySet()){

                if(((SupplierEnterprise)enterprise).getAvaliableMaterialHashMap().containsKey(m)){
                  int oldquantity = ((SupplierEnterprise)enterprise).getAvaliableMaterialHashMap().get(m);
                  int newquantity = oldquantity - d.getBuyorder().getMaterials().get(m);
                  ((SupplierEnterprise)enterprise).getAvaliableMaterialHashMap().put(m, newquantity);
                }
            
                if(((WarehouseOrganization)d.getBuyorder().getReOrganization()).getAvaliableMaterialHashMap().containsKey(m)){
                  int oldquantity = ((WarehouseOrganization)d.getBuyorder().getReOrganization()).getAvaliableMaterialHashMap().get(m);
                  int newquantity = oldquantity - d.getBuyorder().getMaterials().get(m);
                  ((WarehouseOrganization)d.getBuyorder().getReOrganization()).getAvaliableMaterialHashMap().put(m, newquantity);
                }
    }
            populateTable();
             JOptionPane.showMessageDialog(null, "Start!", "Warning", JOptionPane.INFORMATION_MESSAGE);
       }
       else{
           JOptionPane.showMessageDialog(null, "You are busy!", "Warning", JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

