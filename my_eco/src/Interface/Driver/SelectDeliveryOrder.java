    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) OrderjTable.getModel();

        model.setRowCount(0);
        for (DeliveryOrder d : ((Driver)account.getEmployee()).getDeliveryOrderDictionary().getDeliveryOrders()) {
            Object[] row = new Object[3];
            row[0] = d;
            row[1] = d.getBuyorder().getOrderid();
            row[2] = d.getBuyorder().getEnterprise().getName();
            model.addRow(row);
        }
    }
