    private void populateAreaJTable() {
        DefaultTableModel model = (DefaultTableModel) areaJTable.getModel();

        model.setRowCount(0);
        for (Area area : system.getAreaList()) {
            Object[] row = new Object[1];
            row[0] = area.getName();
            model.addRow(row);
        }
    }

